CREATE TABLE ECOMMERCE_QUESTION(

BNO NUMBER(5,0) PRIMARY key, 
TITLE VARCHAR2(100 BYTE) not null, 
CONTENT VARCHAR2(1000 BYTE) not null,
WRITER VARCHAR2(21 BYTE) CONSTRAINT QUESTION_fk REFERENCES ECOMMERCE_USER(USER_ID) ON DELETE CASCADE,
REGDATE DATE DEFAULT sysdate,
UPDATEDATE DATE DEFAULT sysdate, 
VIEWCNT NUMBER(10,0) DEFAULT 0
);
